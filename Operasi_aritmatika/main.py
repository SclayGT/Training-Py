a = 11
b = 3

# Operasi perjumlahan
print("a + b =", a + b)
# Operasi pengurangan
print("a - b =", a - b)
# Operasi perkalian
print("a * b =", a * b)
# Operasi pembagian
print("a / b =", a / b)
# Operasi modulus
print("a % b =", a % b)
# Operasi pangkat
print("a ** b =", a ** b)
# Operasi floor division
print("a // b =", a // b)

print("==========================")

# Prioritas operasi
# 1. ()
# 2. ** (pangkat)
# 3. * / // %
# 4. + -
# Contoh
print("a + b * 2 =", a + b * 2)
print("a * b + 2 =", a * b + 2)
print("(a + b) * 2 =", (a + b) * 2)
print("a * (b + 2) =", a * (b + 2))


